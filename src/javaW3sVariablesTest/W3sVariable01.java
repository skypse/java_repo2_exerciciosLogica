package javaW3sVariablesTest;

public class W3sVariable01 {
    public static void main(String[] args) {
        System.out.println("string = armazena texto exemplo 'olá', valores do tipo string é utilizado aspas duplas");
        System.out.println("int = armazena números inteiros sem os decimais. exemplo '123 ou -123'");
        System.out.println("float = armazena números flutuantes com os decimais, exemplo '19.99 ou -19.99'");
        System.out.println("char = armzana caracteres isolados como 'a' ou 'B', os valores do tipo char é acoplado por aspas simples");
        System.out.println("boolean = armazena o valor de dois estados podendo ser: True or False");

        // exemplo declaração de variável
        System.out.println("exemplo declaracao variavel");
        System.out.println("tipo nomeVariavel = valor");
        // variavel tipo string
        String nome = "Gabriel";
        System.out.println(nome);
        int meuNumero = 15;
        System.out.println(meuNumero);
        float meuNumeroFloat = 15.99f;
        System.out.println(meuNumeroFloat);
        char meuPrimeiroCaractere = 'A';
        System.out.println(meuPrimeiroCaractere);
        boolean meuPrimeiroEstado = true;
        System.out.println(meuPrimeiroEstado);
    }
}

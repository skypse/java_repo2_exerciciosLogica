package javaW3sOperators;

public class W3sOperator02 {
    public static void main(String[] args) {
        /* Em java Ã© divido os operadores em grupos:
         * - Arithmetic operators
         * - Assignment operators
         * - Comparison operators
         * - Logical operators
         * - Bitwise operators
         * */

        // Addition
        int x1 = 10;
        int y1 = 5;
        int z1 = x1 + y1;
        System.out.println(z1);

        // Subtraction
        int x2 = 10;
        int y2 = 5;
        int z2 = x2 - y2;
        System.out.println(z2);

        // Multiplication
        int x3 = 10;
        int y3 = 5;
        int z3 = x3 * y3;
        System.out.println(z3);

        // Division
        int x4 = 10;
        int y4 = 5;
        int z4 = x4 / y4;
        System.out.println(z4);

        // Modulus
        int x5 = 5;
        int y5 = 2;
        int z5 = x5 % y5;
        System.out.println(z5);

        // Increment
        int x6 = 5;
        ++x6;
        System.out.println(x6);

        // Decrement
        int x7 = 5;
        --x7;
        System.out.println(x7);
    }
}
